export function pickRandomPraise(): string {
  const i = Math.floor(Math.random() * SANDBOX_PRAISE_LINES.length);
  return SANDBOX_PRAISE_LINES[i] ?? SANDBOX_PRAISE_LINES[0];
}

/** Стабильная похвала при повторном заходе в урок (не меняется при каждом рендере). */
export function praiseForLessonId(lessonId: string): string {
  let h = 0;
  for (let i = 0; i < lessonId.length; i++) {
    h = (h * 31 + lessonId.charCodeAt(i)) >>> 0;
  }
  return SANDBOX_PRAISE_LINES[h % SANDBOX_PRAISE_LINES.length] ?? SANDBOX_PRAISE_LINES[0];
}
